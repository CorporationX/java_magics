class HogwartsStudent {
    String name;
    int age;
    Wand wand;

    HogwartsStudent(String name) {
        this.name = name;
        this.age = age;
        this.wand = wand;
    }

    HogwartsStudent(String name, int age) {
        this.name = name;
        this.age = age;
        this.wand = null;
    }
}
